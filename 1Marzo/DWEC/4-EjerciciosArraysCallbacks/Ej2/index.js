function sumaPositivos(array){

    const hayPositivos = [] = hayPositivos.push(array.filter((numero) => numero.filer > 0));
    const suma = 0;

    if(array.size == 0 || hayPositivos.size > 0){
        return 0;
    }else{
        for(let i = 0; i < hayPositivos.length; i++){
            suma += hayPositivos[i];        
        }
        return suma;
    }
}